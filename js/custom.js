$(function () {

	"use strict";

	/*===============================================================
	  Preloader
	===============================================================*/

	setTimeout(function () {
		$('.loader_bg').fadeToggle();
	}, 1500);

	/*===============================================================
	  Load Header
	===============================================================*/
	$("#header-box").load("includes/header.html");

	/*===============================================================
	  Load Footer
	===============================================================*/
	$("#footer-box").load("includes/footer.html");

	/*===============================================================
	  Toggle sidebar
	===============================================================*/
	$(document).ready(function () {
		$('#sidebarCollapse').on('click', function () {
			$('#sidebar').toggleClass('active');
			$(this).toggleClass('active');
		});
	});
});

